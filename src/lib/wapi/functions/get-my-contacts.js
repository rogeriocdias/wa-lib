export const getMyContacts = function (done) {
  const contacts = WPP.whatsapp.ContactStore.filter(
    (contact) => contact.isMyContact === true
  ).map((contact) => WAPI._serializeContactObj(contact));
  if (done !== undefined) done(contacts);
  return contacts;
};
